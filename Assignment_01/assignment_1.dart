void main() {
  const a = 25;
  const b = 18;
  // Addition
  int sum = a + b;
  print('Addition: $a+ $b = $sum');
  // Subtraction
  int difference = a - b;
  print('Subtraction: $a - $b = $difference');
  //Multiplication
  int product = a * b;
  print('Multiplication: $a * $b = $product');
  //Division
  double quotient = a / b;
  print('Division: $a / $b = $quotient');
  //Modulus
  int remainder = a % b;
  print('Modulus: $a % $b = $remainder');
}
